class LaptopInfo{
	public String laptopsName;
	public double laptopPrice;
	
	public LaptopInfo(String laptopsName,double laptopPrice){
		this.laptopsName=laptopsName;
		this.laptopPrice=laptopPrice;
	}
	
	public void laptopInfoDisplay(){
		
		System.out.println(laptopsName);
		System.out.println(laptopPrice);
		
		
	}
}